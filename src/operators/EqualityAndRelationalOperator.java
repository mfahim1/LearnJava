package operators;

public class EqualityAndRelationalOperator {
    //EqualityAndRelationalOperator

    // == Equal to
    // != not Equal
    // > Greater than
    // >= Greater than Equal to
    // < Less than
    // <= Less than Equal

    public static void main(String[] args) {

        int num1=50; //declared a variable
        if (num1    ==  50){ //comparing
            //if condition is true, then execute IF block of code
            System.out.println("Number 1 value is equal to 50");
        }
        if (num1    !=  52){ //comparing
            //if condition is true, then execute IF block of code
            System.out.println("Number 1 value is not equal to 50");
        }
        if (num1    >  34){ //comparing
            //if condition is true, then execute IF block of code
            System.out.println("Number 1 value is greater than 50");
        }
        if (num1    >=  50){ //comparing
            //if condition is true, then execute IF block of code
            System.out.println("Number 1 value is greater than or equal to 50");
        }
        if (num1    <  55){ //comparing
            //if condition is true, then execute IF block of code
            System.out.println("Number 1 value is less than 50");
        }
        if (num1    <=  51){ //comparing
            //if condition is true, then execute IF block of code
            System.out.println("Number 1 value is less than or equal to 50");
        }
    }


}
